/**
 * Returns the affirmations list shuffled (Fisher-Yates algorithm).
 * Called once per session so the order feels fresh every time.
 */
export function getShuffledAffirmations() {
  const arr = [...AFFIRMATIONS];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

/**
 * Replaces the {name} placeholder with the actual user name.
 */
export function personalise(affirmation, name) {
  return affirmation.replace(/\{name\}/g, name || 'gorgeous');
}
